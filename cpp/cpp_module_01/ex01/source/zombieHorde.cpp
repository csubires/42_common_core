#include "../include/Zombie.hpp"

Zombie* zombieHorde(int N, std::string name)
{
	Zombie *zombieHorde = new Zombie[N];

	for (int x = 0; x < N; x++)
		zombieHorde[x].setName(name);
	return (zombieHorde);
}
